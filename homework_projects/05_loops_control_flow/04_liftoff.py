def main():
    # Countdown from 10 to 1 using a for loop
    for i in range(10, 0, -1):
        print(i, end=" ")

    # After the countdown, print Liftoff!
    print("Liftoff!")

# This provided line is required at the end of
# Python file to call the main() function.
if __name__ == '__main__':
    main()
